package com.app.model;

public class ClerkSpaceCommentInfo {

	Integer ClerkSpaceID;
	String SpaceComment;
	String Operator;
	String OperateDate;
	public Integer getClerkSpaceID() {
		return ClerkSpaceID;
	}
	public void setClerkSpaceID(Integer clerkSpaceID) {
		ClerkSpaceID = clerkSpaceID;
	}
	public String getSpaceComment() {
		return SpaceComment;
	}
	public void setSpaceComment(String spaceComment) {
		SpaceComment = spaceComment;
	}
	public String getOperator() {
		return Operator;
	}
	public void setOperator(String operator) {
		Operator = operator;
	}
	public String getOperateDate() {
		return OperateDate;
	}
	public void setOperateDate(String operateDate) {
		OperateDate = operateDate;
	}

}
